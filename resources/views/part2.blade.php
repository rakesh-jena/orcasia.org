                                                evidenced by the fact that all Party Secretaries of provinces are part of
                                                the CC, except three. Only Party Secretaries from&nbsp;<strong>Anhui,
                                                    Ningxia and Shanxi are not part of the 19<sup>th</sup>&nbsp;CC and are
                                                    expected to join the 20<sup>th</sup>&nbsp;CC</strong>. Of the 28 Party
                                                Secretaries in the 19<sup>th</sup>&nbsp;CC, 21 are part of the Full CC and 6
                                                are even part of the Politburo (Beijing, Shanghai, Tianjin, Chongqing,
                                                Guangdong and Xinjiang). Provincial Party secretaries, governors and mayors
                                                are a&nbsp;<strong>reliable pool of candidates eligible for higher
                                                    office</strong>, given their experience in handling large, populous and
                                                prosperous regions of China.</p>
