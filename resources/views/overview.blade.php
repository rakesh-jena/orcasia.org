                        role in CPC&rsquo;s decision-making. Amongst these Central Committee members, 24 members were
                        appointed in the Politburo of CPC and seven out of these 24 were appointed to the Politburo Standing
                        Committee &ndash; the most powerful political body in China. Seven members of the Central Military
                        Commission are also from the new Central Committee.</p>
